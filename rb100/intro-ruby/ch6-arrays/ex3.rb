arr = [["test", "hello", "world"],["example", "mem"]]

=begin
arr is an array with 2 array values: ["test", "hello", "world"] and
["example", "mem"]
To return "example", we must first access the last value of arr
  - arr.last
Then, we access the first value of that selection
  - arr.last.first
=end